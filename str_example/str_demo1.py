
# 1.1 字符串的截取
print("1.2截取字符串")
str1 = '123456789'
print(str1)  # 输出字符串
print(str1[0:-1])  # 输出第一个到倒数第二个的所有字符
print(str1[0])  # 输出字符串第一个字符
print(str1[2:5])  # 输出从第三个开始到第五个的字符
print(str1[2:])  # 输出从第三个开始后的所有字符
print(str1[1:5:2])  # 输出从第二个开始到第五个且每隔两个的字符
print(str1 * 2)  # 输出字符串两次
print(str1 + '你好')  # 连接字符串

